-- Tabella mesi
CREATE TABLE months (
  id TEXT PRIMARY KEY,           -- es. "2025-06"
  label TEXT NOT NULL,           -- es. "Giugno 2025"
  saldo_iniziale NUMERIC DEFAULT 0,
  saldo_finale NUMERIC DEFAULT 0,
  risparmiati NUMERIC DEFAULT 0,
  entrate_previste NUMERIC DEFAULT 0,
  entrate_effettive NUMERIC DEFAULT 0,
  uscite_previste NUMERIC DEFAULT 0,
  uscite_effettive NUMERIC DEFAULT 0,
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Tabella transazioni
CREATE TABLE transactions (
  id UUID PRIMARY KEY DEFAULT gen_random_uuid(),
  month_id TEXT REFERENCES months(id) ON DELETE CASCADE,
  data DATE NOT NULL,
  importo NUMERIC NOT NULL,       -- negativo = uscita, positivo = entrata
  descrizione TEXT NOT NULL,
  categoria TEXT NOT NULL,
  created_by UUID REFERENCES auth.users(id),
  created_at TIMESTAMPTZ DEFAULT NOW()
);

-- Indici per performance
CREATE INDEX idx_transactions_month ON transactions(month_id);
CREATE INDEX idx_transactions_data ON transactions(data DESC);

-- ============================================================
-- ROW LEVEL SECURITY - solo utenti autenticati leggono/scrivono
-- ============================================================

ALTER TABLE months ENABLE ROW LEVEL SECURITY;
ALTER TABLE transactions ENABLE ROW LEVEL SECURITY;

-- Mesi: tutti gli utenti autenticati possono leggere e scrivere
CREATE POLICY "auth_read_months" ON months
  FOR SELECT USING (auth.role() = 'authenticated');

CREATE POLICY "auth_write_months" ON months
  FOR ALL USING (auth.role() = 'authenticated');

-- Transazioni: tutti gli utenti autenticati possono leggere e scrivere
CREATE POLICY "auth_read_transactions" ON transactions
  FOR SELECT USING (auth.role() = 'authenticated');

CREATE POLICY "auth_insert_transactions" ON transactions
  FOR INSERT WITH CHECK (auth.role() = 'authenticated');

CREATE POLICY "auth_delete_transactions" ON transactions
  FOR DELETE USING (auth.uid() = created_by);

